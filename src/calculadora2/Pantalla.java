    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        int numero1 = Integer.parseInt(this.numero1.getText());
        int numero2 = Integer.parseInt(this.numero2.getText());

        switch (this.operador.getText()) {
            case "+":
                this.resultado.setText("" + (numero1 + numero2));
                break;
            case "-":
                this.resultado.setText("" + (numero1 - numero2));
                break;
            case "*":
                this.resultado.setText("" + (numero1 * numero2));
                break;
            case "/":
                if (numero2 == 0) {
                    this.resultado.setText("no se puede dividir");
                } else {
                    this.resultado.setText("" + (numero1 / numero2));
                }

                break;
            default:
                this.resultado.setText("Opci√≥n no disponible");
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

